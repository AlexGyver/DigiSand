void printIcon(GyverGFX* mx, int X, int Y, uint8_t icon) {
    for (uint8_t x = 0; x < 8; x++) {
        uint8_t col = pgm_read_byte(&icons[icon][x]);
        for (uint8_t y = 0; y < 8; y++) {
            if (bitRead(col, y)) mx->dot(X + x, Y + y);
        }
    }
}