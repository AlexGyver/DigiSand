void printDig(GyverGFX* mx, int X, int Y, uint8_t dig) {
    if (dig > 9) return;
    for (uint8_t x = 0; x < 3; x++) {
        uint8_t col = pgm_read_byte(&font3x5[dig][x]);
        for (uint8_t y = 0; y < 5; y++) {
            if (bitRead(col, 4 - y)) mx->dot(X + 2 - x, Y + y);
        }
    }
}

